#!/usr/bin/env python3

# Ask the user for their age
age = int(input("Enter your age: "))

# Calculate future ages
age_10 = age + 10
age_20 = age + 20
age_30 = age + 30

# Display results
print(f"In 10 years, you will be {age_10} years old.")
print(f"In 20 years, you will be {age_20} years old.")
print(f"In 30 years, you will be {age_30} years old.")
