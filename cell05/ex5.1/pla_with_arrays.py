#!/usr/bin/env python3

# Define the original array of numbers
original_array = [2, 8, 9, 48, 8, 22, -12, 2]

# Create a new array by adding 2 to each element of the original array
new_array = [num + 2 for num in original_array]

# Display both arrays
print("Original array:", original_array)
print("New array:", new_array)
